import 'dart:io';

final class HomeEntryBackRule {
  const HomeEntryBackRule({
    required this.id,
    required this.area,
    required this.file,
    required this.requiredSnippets,
    required this.notes,
    this.forbiddenSnippets = const [],
  });

  final String id;
  final String area;
  final String file;
  final List<String> requiredSnippets;
  final List<String> forbiddenSnippets;
  final String notes;
}

final class HomeEntryBackFinding {
  const HomeEntryBackFinding({
    required this.rule,
    required this.fileExists,
    required this.missingRequired,
    required this.presentForbidden,
    required this.line,
  });

  final HomeEntryBackRule rule;
  final bool fileExists;
  final List<String> missingRequired;
  final List<String> presentForbidden;
  final int line;

  bool get passed =>
      fileExists && missingRequired.isEmpty && presentForbidden.isEmpty;

  String get status => passed ? 'PASS' : 'FAIL';

  String get evidence {
    if (!fileExists) return 'missing file';
    if (missingRequired.isNotEmpty) {
      return 'missing: ${missingRequired.join(' | ')}';
    }
    if (presentForbidden.isNotEmpty) {
      return 'forbidden: ${presentForbidden.join(' | ')}';
    }
    return line > 0 ? 'line $line' : 'present';
  }
}

void main(List<String> args) {
  final checkOnly = args.contains('--check');
  final appRoot = _findAppRoot();
  final repoRoot = appRoot.uri.resolve('..').toFilePath();
  final docsDir = Directory('${repoRoot}docs/02_FLUTTER_MIGRATION');
  final markdownFile = File(
    '${docsDir.path}/VitTrade-Home-Entry-Back-Navigation-Audit.md',
  );
  final csvFile = File(
    '${docsDir.path}/VitTrade-Home-Entry-Back-Navigation-Audit.csv',
  );

  final findings = _rules.map((rule) => _evaluate(rule, appRoot)).toList();
  final markdown = _renderMarkdown(findings);
  final csv = _renderCsv(findings);
  final summary = _renderSummary(findings);

  if (checkOnly) {
    final failures = <String>[];
    if (!markdownFile.existsSync()) {
      failures.add('Home entry back navigation markdown artifact is missing.');
    } else if (markdownFile.readAsStringSync() != markdown) {
      failures.add('Home entry back navigation markdown artifact is stale.');
    }

    if (!csvFile.existsSync()) {
      failures.add('Home entry back navigation CSV artifact is missing.');
    } else if (csvFile.readAsStringSync() != csv) {
      failures.add('Home entry back navigation CSV artifact is stale.');
    }

    final failedRules = findings.where((finding) => !finding.passed).toList();
    if (failedRules.isNotEmpty) {
      failures.add(
        'Home entry back navigation audit found '
        '${failedRules.length} failing rule(s).',
      );
    }

    if (failures.isNotEmpty) {
      for (final failure in failures) {
        stderr.writeln(failure);
      }
      stderr.writeln(
        'Run `dart run tool/home_entry_back_navigation_audit.dart` '
        'from flutter_app/.',
      );
      exitCode = 1;
      return;
    }

    stdout.write(summary);
    stdout.writeln('Home entry back navigation artifacts are current.');
    return;
  }

  docsDir.createSync(recursive: true);
  markdownFile.writeAsStringSync(markdown);
  csvFile.writeAsStringSync(csv);
  stdout.writeln('Wrote ${markdownFile.path}');
  stdout.writeln('Wrote ${csvFile.path}');
  stdout.write(summary);
}

HomeEntryBackFinding _evaluate(HomeEntryBackRule rule, Directory appRoot) {
  final file = File('${appRoot.path}/${rule.file}');
  if (!file.existsSync()) {
    return HomeEntryBackFinding(
      rule: rule,
      fileExists: false,
      missingRequired: rule.requiredSnippets,
      presentForbidden: const [],
      line: 0,
    );
  }

  final source = file.readAsStringSync();
  final missingRequired = [
    for (final snippet in rule.requiredSnippets)
      if (!source.contains(snippet)) snippet,
  ];
  final presentForbidden = [
    for (final snippet in rule.forbiddenSnippets)
      if (source.contains(snippet)) snippet,
  ];
  final line = _firstSnippetLine(source, rule.requiredSnippets);

  return HomeEntryBackFinding(
    rule: rule,
    fileExists: true,
    missingRequired: missingRequired,
    presentForbidden: presentForbidden,
    line: line,
  );
}

String _renderMarkdown(List<HomeEntryBackFinding> findings) {
  final buffer = StringBuffer()
    ..writeln('# VitTrade Home Entry Back Navigation Audit')
    ..writeln()
    ..writeln(
      'Generated by `dart run tool/home_entry_back_navigation_audit.dart`.',
    )
    ..writeln()
    ..writeln(_renderSummary(findings).trimRight())
    ..writeln()
    ..writeln('| ID | Area | File | Status | Evidence | Notes |')
    ..writeln('| --- | --- | --- | --- | --- | --- |');

  for (final finding in findings) {
    buffer.writeln(
      '| ${finding.rule.id} '
      '| ${finding.rule.area} '
      '| `${finding.rule.file}` '
      '| ${finding.status} '
      '| ${_escapeTable(finding.evidence)} '
      '| ${_escapeTable(finding.rule.notes)} |',
    );
  }

  return buffer.toString();
}

String _renderCsv(List<HomeEntryBackFinding> findings) {
  final rows = [
    ['id', 'area', 'file', 'status', 'evidence', 'notes'],
    for (final finding in findings)
      [
        finding.rule.id,
        finding.rule.area,
        finding.rule.file,
        finding.status,
        finding.evidence,
        finding.rule.notes,
      ],
  ];

  return '${rows.map((row) => row.map(_escapeCsv).join(',')).join('\n')}\n';
}

String _renderSummary(List<HomeEntryBackFinding> findings) {
  final passed = findings.where((finding) => finding.passed).length;
  final failed = findings.length - passed;
  return 'home_entry_back_rules=${findings.length}\n'
      'passed=$passed\n'
      'failed=$failed\n';
}

Directory _findAppRoot() {
  final current = Directory.current;
  if (Directory('${current.path}/lib/app/router/route_groups').existsSync()) {
    return current;
  }

  final nested = Directory('${current.path}/flutter_app');
  if (Directory('${nested.path}/lib/app/router/route_groups').existsSync()) {
    return nested;
  }

  throw StateError('Run from repo root or flutter_app/.');
}

int _firstSnippetLine(String source, List<String> snippets) {
  for (final snippet in snippets) {
    final index = source.indexOf(snippet);
    if (index < 0) continue;
    return '\n'.allMatches(source.substring(0, index)).length + 1;
  }
  return 0;
}

String _escapeTable(String value) {
  return value.replaceAll('|', r'\|').replaceAll('\n', '<br>');
}

String _escapeCsv(String value) {
  final escaped = value.replaceAll('"', '""');
  return '"$escaped"';
}

